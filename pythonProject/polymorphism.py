class Shape:
    def area(self):
        return "Undefined"


class Rectangle(Shape):
    def __init__(self, length, width):
        self.length = length
        self.width = width

    def area(self):
        return self.length * self.width


class Circle(Shape):
    def __init__(self, radius):
        self.radius = radius

    def area(self):
        return 3.14 * self.radius ** 2


class Square(Shape):
    def __init__(self, side):
        self.side = side

    def area(self):
        return self.side ** 2


shapes = [Rectangle(2, 3), Circle(5), Square(10)]
for shape in shapes:
    print(f"Area: {shape.area()}")
